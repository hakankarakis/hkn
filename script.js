var soruIndex = 0;
var cevapGosterildi = false;

function guncelleSoruCevap() {
  var soruElement = document.getElementById("question");
  var cevapElement = document.getElementById("answer");

  soruElement.textContent = sorular[soruIndex].soru;
  cevapElement.textContent = sorular[soruIndex].cevap;
  cevapElement.style.display = "none";
  cevapGosterildi = false;
}

function toggleAnswer() {
  var cevapElement = document.getElementById("answer");
  cevapGosterildi = !cevapGosterildi;

  if (cevapGosterildi) {
    cevapElement.style.display = "block";
  } else {
    cevapElement.style.display = "none";
  }
}

function karisikSorulariGoster() {
  var soruAlani = $('#soru-alani');
  var sorular = soruAlani.children();

  if ($('#karisik-sorular').is(':checked')) {
    // Soruları karışık sırala
    sorular.sort(function() {
      return 0.5 - Math.random();
    });
    soruAlani.empty().append(sorular);
  } else {
    // Soruları orijinal sıralama ile göster
    sorular.sort(function(a, b) {
      return $(a).data('sira') - $(b).data('sira');
    });
    soruAlani.empty().append(sorular);
  }
}
function previousQuestion() {
  if (soruIndex > 0) {
    soruIndex--;
    guncelleSoruCevap();
  }
}

function nextQuestion() {
  if (soruIndex < sorular.length - 1) {
    soruIndex++;
    guncelleSoruCevap();
  }
}

